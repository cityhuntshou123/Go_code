package main

import "fmt"

func main() {
	var a string = "Runoob"
	fmt.Println(a)
	
	var b, c int = 1, 2
	fmt.Println(b, c)
	
	// 变量声明
	var a_string = "RUNOOB"
	fmt.Println(a_string)
	
	// 没有初始化就为零值
	var int_0 int
	fmt.Println(int_0)
	
	// bool 零值为 false
	var bool_false bool
	fmt.Println(bool_false)
}